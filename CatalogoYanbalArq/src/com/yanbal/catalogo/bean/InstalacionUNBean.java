package com.yanbal.catalogo.bean;

public class InstalacionUNBean {

	private Integer corInstalacion;
	private Integer idUnidadNegocio;
	private String nombreUN;
	private String tipoInstalacion;
	private Integer idCluster;
	private Integer idServidor;
	private String nombreInstalacion;
	private String nombreFisico;
	
	public Integer getCorInstalacion() {
		return corInstalacion;
	}
	public void setCorInstalacion(Integer corInstalacion) {
		this.corInstalacion = corInstalacion;
	}
	public Integer getIdUnidadNegocio() {
		return idUnidadNegocio;
	}
	public void setIdUnidadNegocio(Integer idUnidadNegocio) {
		this.idUnidadNegocio = idUnidadNegocio;
	}
	public String getNombreUN() {
		return nombreUN;
	}
	public void setNombreUN(String nombreUN) {
		this.nombreUN = nombreUN;
	}
	public String getTipoInstalacion() {
		return tipoInstalacion;
	}
	public void setTipoInstalacion(String tipoInstalacion) {
		this.tipoInstalacion = tipoInstalacion;
	}
	public Integer getIdCluster() {
		return idCluster;
	}
	public void setIdCluster(Integer idCluster) {
		this.idCluster = idCluster;
	}
	public Integer getIdServidor() {
		return idServidor;
	}
	public void setIdServidor(Integer idServidor) {
		this.idServidor = idServidor;
	}
	public String getNombreInstalacion() {
		return nombreInstalacion;
	}
	public void setNombreInstalacion(String nombreInstalacion) {
		this.nombreInstalacion = nombreInstalacion;
	}
	public String getNombreFisico() {
		return nombreFisico;
	}
	public void setNombreFisico(String nombreFisico) {
		this.nombreFisico = nombreFisico;
	}

}
