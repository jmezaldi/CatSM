package com.yanbal.catalogo.bean;

public class TablasCodigosBean {
	
	private Integer codTabla;
	private Integer codDato;
	private String  desAbreviada;
	private String  desCompleta;
	private Integer estado;
	public Integer getCodTabla() {
		return codTabla;
	}
	public void setCodTabla(Integer codTabla) {
		this.codTabla = codTabla;
	}
	public Integer getCodDato() {
		return codDato;
	}
	public void setCodDato(Integer codDato) {
		this.codDato = codDato;
	}
	public String getDesAbreviada() {
		return desAbreviada;
	}
	public void setDesAbreviada(String desAbreviada) {
		this.desAbreviada = desAbreviada;
	}
	public String getDesCompleta() {
		return desCompleta;
	}
	public void setDesCompleta(String desCompleta) {
		this.desCompleta = desCompleta;
	}
	public Integer getEstado() {
		return estado;
	}
	public void setEstado(Integer estado) {
		this.estado = estado;
	}


}
